     * index.php/api/Homestay?page=2
     * @return \think\Response
     */
    public function index()
    {
        //
        $data = $this->request->get();
        //分页
        if (isset($data['page'])&&!empty($data['page'])){
            $page = $data['page'];
        }else{
            $page = 1;
        }
        if (isset($data['limit'])&&!empty($data['limit'])){
            $limit = $data['limit'];
        }else{
            $limit = config('homestayLimit');
        }
        //处理搜索条件
        $where =[];
        if (isset($data['hcity'])&&!empty($data['hcity'])){
            $where['hcity'] =['like','%'.$data['hcity'].'%'];
        }
        if (isset($data['hname'])&&!empty($data['hname'])){
            $where['hname'] = ['like','%'.$data['hname'].'%'];
        }
        if (isset($data['cname'])&&!empty($data['cname'])){
            $where['cname'] = $data['cname'];
        }
//        $result = Db::table('homestay')->alias('h')->join(['category'=>'c'],'h.cid = c.cid','left')->field('hid,cname,hname,hprovince,hcity,haddress,')->where($where)->paginate($limit,false,['page'=>$page]);
        $result = Db::view('homestay','hid,hname,hdesc,hprovince,hcity,harea,haddress,hprice,hscore,hstatus,htag,htime')->view('category','cname','homestay.cid=category.cid')->where($where)->paginate($limit,false,['page'=>$page]);
        $homestay = $result->items();
        $total = $result->total();
        if($homestay&&$total){
            return json([
                'code'=>$this->code['success'],
                'msg'=>'数据查询成功',
                'total'=>$total,
                'data'=>$homestay
            ]);
        }else{
            return json([
                'code'=>$this->code['fail'],
                'msg'=>'数据添加失败'
            ]);
        }
    }

