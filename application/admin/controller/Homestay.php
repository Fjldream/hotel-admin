    /**
     * 保存新建的资源
     *
     * @param \think\Request $request
     * @return \think\Response
     */
    public function save(Request $request)
    {
        $data = $this->request->post();
        //$this->validate->sence()->check();
        $hname = $data['hname'];
        $isExist = Db::table('homestay')->where('hname','=',$hname)->count();
        if($isExist){
            return json([
                'code'=>$this->code['fail'],
                'msg'=>'该民宿名已经存在'
            ]);
        }

        $data['htime'] =time();
        $result = Db::table('homestay')->insert($data);
        if($result){
            return json([
                'code'=>$this->code['success'],
                'msg'=>'数据添加成功',
            ]);
        }else{
            return json([
                'code'=>$this->code['fail'],
                'msg'=>'数据添加失败'
            ]);
        }
    }

    /**
     * 显示指定的资源
